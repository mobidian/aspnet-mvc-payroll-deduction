        public override MembershipUser GetUser(string username, bool userIsOnline)
        {
            var user = UserStore.All().SingleOrDefault(u => u.Username == username);

            if (user == null)
                return null;

            return new MembershipUser(
                providerName: "SimpleMembershipProvider",
                name: user.Username,
                providerUserKey: user.Username,
                email: user.Username,
                passwordQuestion: null,
                comment: null,
                isApproved: true,
                isLockedOut: false,
                creationDate: DateTime.Now,
                lastLoginDate: DateTime.Now,
                lastActivityDate: DateTime.Now,
                lastPasswordChangedDate: new DateTime(),
                lastLockoutDate: new DateTime()
            );
        }

        public override bool ValidateUser(string username, string password)
        {
            return UserStore.All().Any(u => u.Username == username && u.Password == password);
        }
